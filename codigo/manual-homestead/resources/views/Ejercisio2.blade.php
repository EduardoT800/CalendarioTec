			<h2>El personaje</h2>
			<p>
			Sonic Es el personaje principal de la saga con su mismo nombre; también está presente en cómics, dibujos animados y libros. El primer videojuego de esta franquicia fue creado en 1991, para competir con los videojuegos de la mascota de Nintendo: Mario, reemplazando a Alex Kidd. Sonic es un erizo azul antropomórfico que (según la versión de los cómics y juegos) él tiene la habilidad de correr a la velocidad del sonido, una característica que se muestra en la mayoría de sus juegos siendo, además, su seña de identidad, conforme ha pasado el tiempo Sonic ha ido desarrollando su velocidad a tal punto que ha alcanzado la velocidad de la luz2​ e incluso ha podido superarla.
			</p>

			<p>
			El grupo interno de desarrollo de Sega AM8 realizó la mayoría de los bocetos del personaje, entre los cuales estuvo un armadillo (después aparecería Mighty), un hombre obeso con aspecto humanoide (que luego sería la base del enemigo de Sonic, el Dr. Eggman) y un conejo. La opción que se eligió definitivamente fue un erizo, diseñado por Naoto Ohshima, que sería llamado «Sonic».
			</p>
			
			<p>
			AM8 comenzó el desarrollo del juego y pasó a llamarse desde entonces hasta la actualidad como Sonic Team (De hecho, excepto AM2, todos los grupos AM# de Sega han terminado modificando sus nombres).
			</p>

			<p>			
			El diseño ha variado levemente con el paso del tiempo, aunque a rasgos generales siempre ha sido el mismo. Los cambios de diseño más destacados que ha sufrido el personaje se produjeron primero, más levemente, con el lanzamiento de Sonic the Hedgehog 2 cambiando la estructura de la cabeza y las púas que más tarde sería rectificado y volverían al modelo antiguo, y segundo, más destacado, con el lanzamiento de Sonic Adventure en 1998 con el objetivo de conseguir una nueva imagen corporativa de Sega más moderna, optando por trazados más dinámicos y flexibles, diseños más estilizados, y otorgándole iris verde a los ojos (que hasta entonces, desde su creación, eran negros completamente). Este cambio estético también se produjo en Tails, Amy, Knuckles, Dr. Eggman, Metal Sonic, Espio y prácticamente todos los nuevos personajes siguieron esa misma línea.
			</p>

			<p>
			Según Sonic the cómic, Sonic era en un principio marrón, pero al alcanzar la velocidad del sonido (gracias a las zapatillas que le dio el Dr. Eggman) se vuelve azul. Se dice que Sega quiso darle el mismo color azul que las letras del logotipo de la empresa. Sonic es el personaje de videojuegos más famoso y conocido a nivel mundial, junto con Mario. Según Archie Comics, Sonic tiene los ojos verdes por la Esmeralda Maestra. 
